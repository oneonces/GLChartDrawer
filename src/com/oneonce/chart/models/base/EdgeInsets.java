public class EdgeInsets {
	public float left = 0f;
	public float top = 0f;
	public float right = 0f;
	public float bottom = 0f;

	
	public EdgeInsets(float left, float top, float right, float bottom) {
		this.left = left;
		this.top = top;
		this.right = right;
		this.bottom = bottom;
	}
}
